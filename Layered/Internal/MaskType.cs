namespace Layered.Internal {

	/// <summary>Specifies, which type of mask to generate.</summary>
	internal enum MaskType {

		/// <summary>Top left corner for vertical side</summary>
		VTopLeft,
		
		/// <summary>Top right corner for vertical side</summary>
		VTopRight,
		
		/// <summary>Bottom left corner for vertical side</summary>
		VBottomLeft,

		/// <summary>Bottom right corner for vertical side</summary>
		VBottomRight,

		/// <summary>Top left corner for horizontal side</summary>
		HTopLeft,

		/// <summary>Top right corner for horizontal side</summary>
		HTopRight,

		/// <summary>Bottom left corner for horizontal side</summary>
		HBottomLeft,

		/// <summary>Bottom right corner for horizontal side</summary>
		HBottomRight,

	}

}
